package ch02.sec06;

public class StringExample {

	public static void main(String[] args) {
		String name = "Ria";
		String job = "Student";
		System.out.println(name);
		System.out.println(job);
		
		String str = "I am \'Ria\'";
		System.out.println(str);
		
		str = "Num\tName\tJob";
		System.out.println(str);
		
		System.out.print("Hi\n");	
		System.out.print("Bye");	
	}
}
